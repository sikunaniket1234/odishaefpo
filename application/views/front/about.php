      <p>The importance of institutional credit in boosting rural economy has been clear to the Government of India right from its early stages of planning. Therefore, the Reserve Bank of India (RBI) at the insistence of the Government of India, constituted a Committee to Review the Arrangements For Institutional Credit for Agriculture and Rural Development (CRAFICARD) to look into these very critical aspects. The Committee was formed on 30 March 1979, under the Chairmanship of Shri B. Sivaraman, former member of Planning Commission, Government of India.</p>
      <p>The Committee’s interim report, submitted on 28 November 1979, outlined the need for a new organisational device for providing undivided attention, forceful direction and pointed focus to credit related issues linked with rural development. Its recommendation was formation of a unique development financial institution which would address these aspirations and formation of National Bank for Agriculture and Rural Development (NABARD) was approved by the Parliament through Act 61 of 1981.</p>
      <p>NABARD came into existence on 12 July 1982 by transferring the agricultural credit functions of RBI and refinance functions of the then Agricultural Refinance and Development Corporation (ARDC). It was dedicated to the service of the nation by the late Prime Minister Smt. Indira Gandhi on 05 November 1982. Set up with an initial capital of Rs.100 crore, its’ paid up capital stood at Rs.14,080 crore as on 31 March 2020. Consequent to the revision in the composition of share capital between Government of India and RBI, NABARD today is fully owned by Government of India.</p>
